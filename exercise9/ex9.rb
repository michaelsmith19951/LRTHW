# Set a variable called "days" equal to the days of the week
days = "Mon Tue Wed Thu Fri Sat Sun"
# Set a variable called "months" equal to the months of the year, with the "n" value in front of each month. The "n" value places each month on its own line.
months = "Jan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

# Print the days of the week to the console
puts "Here are the days: #{days}"
# Print the months of the week to the console
puts "Here are the months: #{months}"

